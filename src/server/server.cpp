// Handles the `PUBLISH` command.
static void do_publish(std::vector<std::string> &cmd, Buffer &out) {
    const std::string &channel_name = cmd[1];

    std::string message = cmd[2];
    for (size_t i = 3; i < cmd.size(); ++i) {
        message += " " + cmd[i];
    }


    int receivers = 0;

    // Find the list of subscribers for the given channel.
    auto it = g_data.pubsub_channels.find(channel_name);
    if (it != g_data.pubsub_channels.end()) {
        // Prepare the message that will be sent to all subscribers.
        // Format: ["message", "channel_name", "the_message"]
        std::cout << "PUBLISH: " << channel_name << " with message: " << message << std::endl;
        Buffer pub_message;
        size_t header_pos = 0;
        response_begin(pub_message, &header_pos); // Prepares the 4-byte length header
        out_arr(pub_message, 3);
        out_str(pub_message, "message", 7);
        out_str(pub_message, channel_name.data(), channel_name.size());
        out_str(pub_message, message.data(), message.size());
        response_end(pub_message, header_pos); // Fills in the correct length

        // Send the message to each subscriber.
        for (Conn *sub_conn : it->second) {
            buf_append(sub_conn->outgoing, pub_message.data(), pub_message.size());
            sub_conn->want_write = true; // Tell the event loop to send the data.
            receivers++;
        }
    }
    // std::cout << "PUBLISH: " << channel_name << " to " << receivers << " subscribers." << std::endl;
    // std::cout << "Message: " << message << std::endl;
    
    //print out
    std::cout<<"out"<<std::endl;
    for(auto x:out){
        std::cout << x;
    }
    std::cout << std::endl;

    // Respond to the publisher with the number of clients that received the message.
    return out_int(out, receivers);
}

static void heap_delete(std::vector<HeapItem> &a, size_t pos)
{
    // swap the erased item with the last item
    a[pos] = a.back();
    a.pop_back();
    // update the swapped item
    if (pos < a.size())
    {
        heap_update(a.data(), pos, a.size());
    }
}

static void heap_upsert(std::vector<HeapItem> &a, size_t pos, HeapItem t)
{
    if (pos < a.size())
    {
        a[pos] = t; // update an existing item
    }
    else
    {
        pos = a.size();
        a.push_back(t); // or add a new item
    }
    heap_update(a.data(), pos, a.size());
}

// set or remove the TTL
static void entry_set_ttl(Entry *ent, int64_t ttl_ms)
{
    if (ttl_ms < 0 && ent->heap_idx != (size_t)-1)
    {
        // setting a negative TTL means removing the TTL
        heap_delete(g_data.heap, ent->heap_idx);
        ent->heap_idx = -1;
    }
    else if (ttl_ms >= 0)
    {
        // add or update the heap data structure
        uint64_t expire_at = get_monotonic_msec() + (uint64_t)ttl_ms;
        HeapItem item;
        item.val = expire_at;
        item.ref = &ent->heap_idx;
        heap_upsert(g_data.heap, ent->heap_idx, item);
    }
}

