static void do_decr(std::vector<std::string> &cmd, Buffer &out){
    LookupKey key;
    key.key = cmd[1];
    key.node.hcode = str_hash((uint8_t *)key.key.data(), key.key.size());

    HNode *node = hm_lookup(&g_data.db, &key.node, &entry_eq);

    if(node){
        Entry *ent = container_of(node, Entry, node);

        if (ent->type != T_STR)
        {
            return out_err(out, ERR_BAD_TYP, "WRONGTYPE Operation against a key holding the wrong kind of value");
        }
        std::cout<<ent->str[0]<<std::endl;
        if(ent->str[1]=='0'){
            std::cout << "value is not an integer or out of range" << std::endl;
            return out_err(out, ERR_BAD_ARG, "value is not an integer or out of range");
        }
        // Convert the string to an integer
        int64_t value = std::stoll(strip_quotes(ent->str));
        // Decrement the value
        value--;
        // Update the entry with the new value
        ent->str = "\"" + std::to_string(value) + "\"";
        // Return the new value
        return out_int(out, value);
    }
    // If the key does not exist, create it with a value of -1
    Entry *ent = entry_new(T_STR);
    ent->key = key.key;
    ent->node.hcode = key.node.hcode;
    ent->str = "\"-1\""; // Initialize with -1
    hm_insert(&g_data.db, &ent->node);
    // Return -1 as the new value
    return out_int(out, -1);

}

