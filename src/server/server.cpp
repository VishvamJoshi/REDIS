// application callback when the listening socket is ready
static int32_t handle_accept(SOCKET fd)
{
    // accept
    struct sockaddr_in client_addr = {};
    int addrlen = sizeof(client_addr);
    SOCKET connfd = accept(fd, (struct sockaddr *)&client_addr, &addrlen);
    if (connfd == INVALID_SOCKET)
    {
        msg_errno("accept() error");
        return -1;
    }
    uint32_t ip = client_addr.sin_addr.s_addr;
    fprintf(stderr, "new client from %u.%u.%u.%u:%u\n",
            ip & 255, (ip >> 8) & 255, (ip >> 16) & 255, ip >> 24,
            ntohs(client_addr.sin_port));

    // set the new connection fd to nonblocking mode
    fd_set_nb(connfd);

    // create a `struct Conn`
    Conn *conn = new Conn();
    conn->fd = connfd;
    conn->want_read = true;
    conn->last_active_ms = get_monotonic_msec();
    dlist_insert_before(&g_data.idle_list, &conn->idle_node);

    // put it into the map
    if (g_data.fd2conn.size() <= (size_t)conn->fd)
    {
        g_data.fd2conn.resize(conn->fd + 1);
    }
    assert(!g_data.fd2conn[conn->fd]);
    g_data.fd2conn[conn->fd] = conn;

    return 0;
}

static void conn_destroy(Conn *conn)
{

    if (conn->is_subscribed) {
        for (const std::string& channel_name : conn->subscribed_channels) {
            auto it = g_data.pubsub_channels.find(channel_name);
            if (it != g_data.pubsub_channels.end()) {
                std::vector<Conn*>& subscribers = it->second;
                // Remove the connection pointer from the vector.
                auto to_remove = std::remove(subscribers.begin(), subscribers.end(), conn);
                subscribers.erase(to_remove, subscribers.end());

                // If no subscribers are left, remove the channel itself to save memory.
                if (subscribers.empty()) {
                    g_data.pubsub_channels.erase(it);
                }
            }
        }
    }

    closesocket(conn->fd);
    g_data.fd2conn[conn->fd] = NULL;
    dlist_detach(&conn->idle_node);
    delete conn;
}

const size_t k_max_args = 200 * 1000;

static bool read_u32(const uint8_t *&cur, const uint8_t *end, uint32_t &out)
{
    if (cur + 4 > end)
    {
        return false;
    }
    memcpy(&out, cur, 4);
    cur += 4;
    return true;
}

static bool
read_str(const uint8_t *&cur, const uint8_t *end, size_t n, std::string &out)
{
    if (cur + n > end)
    {
        return false;
    }
    out.assign(cur, cur + n);
    cur += n;
    return true;
}

// ------------------------------------------------------------
// | nstr | len | str1 | len | str2 | ... | len | strn |
// ------------------------------------------------------------

static int32_t
parse_req(const uint8_t *data, size_t size, std::vector<std::string> &out)
{
    const uint8_t *end = data + size;
    uint32_t nstr = 0;
    if (!read_u32(data, end, nstr))
    {
        return -1;
    }
    if (nstr > k_max_args)
    {
        return -1; // safety limit
    }

    while (out.size() < nstr)
    {
        uint32_t len = 0;
        if (!read_u32(data, end, len))
        {
            return -1;
        }
        out.push_back(std::string());
        if (!read_str(data, end, len, out.back()))
        {
            return -1;
        }
    }
    if (data != end)
    {
        return -1; // trailing garbage
    }
    return 0;
}

